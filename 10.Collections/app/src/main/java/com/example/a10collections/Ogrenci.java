package com.example.a10collections;

public class Ogrenci {
    private int tcNo;
    private String ad;

    public Ogrenci(int tcNo, String ad) {
        this.tcNo = tcNo;
        this.ad = ad;
    }

    public int getTcNo() {
        return tcNo;
    }

    public void setTcNo(int tcNo) {
        this.tcNo = tcNo;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }


}
