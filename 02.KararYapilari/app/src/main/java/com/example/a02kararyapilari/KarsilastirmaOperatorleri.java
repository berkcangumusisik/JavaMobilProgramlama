package com.example.a02kararyapilari;

public class KarsilastirmaOperatorleri {
    public static void main(String[] args) {
        int sayi1 = 10;
        int sayi2 = 20;
        System.out.println(sayi1 == sayi2); // false
        System.out.println(sayi1 != sayi2); // true
        System.out.println(sayi1 > sayi2); // false
        System.out.println(sayi1 < sayi2); // true
        System.out.println(sayi1 >= sayi2); // false
        System.out.println(sayi1 <= sayi2); // true

    }
}
/**
 * Karşılaştırma Operatörleri
 * == Eşittir
 * != Eşit değildir
 * > Büyüktür
 * < Küçüktür
 * >= Büyük eşittir
 * <= Küçük eşittir
 */