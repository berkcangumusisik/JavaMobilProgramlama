package com.example.a01degiskenveveriturleri;

public class StringBirlestirme {
    public static void main(String[] args) {
        String ad = "Berkcan";
        String soyad = "Gümüşışık";
        String mesaj = ad + " " + soyad;
        System.out.println(mesaj);

        int sayi1 = 10;
        int sayi2 = 20;
        int mesaj2 = sayi1 + sayi2;
        System.out.println(mesaj2);
    }
}
